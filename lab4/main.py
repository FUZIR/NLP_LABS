    # ПОПЕРЕДНЯ ОБРОБКА (NLP)
    ukr_stopwords = {'і', 'та', 'на', 'в', 'у', 'з', 'до', 'що', 'як', 'це', 'про', 'але', 'за', 'від', 'по', 'для', 'не'}

    def preprocess_text(text):
        text = str(text).lower()
        # Видалення пунктуації
        text = text.translate(str.maketrans('', '', string.punctuation + '«»–—'))
        tokens = text.split()
        # Фільтрація стоп-слів та дуже коротких токенів
        tokens = [t for t in tokens if t not in ukr_stopwords and len(t) > 2]
        return " ".join(tokens)

    df_combined['clean_text'] = df_combined['text'].apply(preprocess_text)

